import {Schema, Model} from 'mongoose'

const registerSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    name: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    }
})

const RegisterModel=new Model('Register',registerSchema)
export default RegisterModel

